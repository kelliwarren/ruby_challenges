
class Car


  def drive(direction)
    puts "You are driving #{direction}"
  end

  def speed(x)
    puts "You are driving #{x} mph"
  end

  def brake
    puts "Whoa, you are slowing down"
  end 

  def accelerate(speed)
    puts "Now you are driving #{speed} mph"
  end

  def decelerate(speed)
    puts "You are now driving #{speed} mph"
  end

end
